package com.baidubupt.coupletserver.entity;

public class Couplet {

    /**
     * 上联
     */
    private String firstCouplet;

    /**
     * 下联
     */
    private String secondCouplet;

    public String getFirstCouplet() {
        return firstCouplet;
    }

    public void setFirstCouplet(String firstCouplet) {
        this.firstCouplet = firstCouplet;
    }

    public String getSecondCouplet() {
        return secondCouplet;
    }

    public void setSecondCouplet(String secondCouplet) {
        this.secondCouplet = secondCouplet;
    }

}
